/*
	GETDATE()
	Devuelve la fecha y hora actuales del sistema en la zona horaria local del 
	servidor SQL Server.

	GETUTCDATE()
	Devuelve la fecha y hora actuales del sistema en formato UTC, 
	lo que es útil para comparaciones y cálculos de tiempo en un contexto global.
*/
SELECT GETDATE()
SELECT GETUTCDATE()
